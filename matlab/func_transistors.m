function [ f ] = func_resistors( x )
% Particle Swarm Optimization: Performance Tuning and Empirical Analysis

g = [0.485 0.752 0.869 0.982; ...
    0.369 1.254 0.703 1.455; ...
    5.2095 10.0677 22.9274 20.2153; ...
    23.3037 101.779 111.461 191.267; ...
    28.5132 111.8467 134.3884 211.4823]

gamma = x(1) * x(3) - x(2) * x(4)

alpha = (1 - x(1) * x(2)) * x(3) * ...
    (exp(x(5) * (g(1,:) - g(3,:) * x(7) / 1000 - g(5,:) * x(8) / 1000))-1) - g(5,:) + ...
    g(4,:) * x(2)
    
beta = (1 - x(1) * x(2)) * x(4) * ...
    (exp(x(6) * (g(1,:) - g(2,:) - g(3,:) * x(7) / 1000 + g(4,:) * x(9) / 1000))-1) - g(5,:) * x(1) + g(4,:)

f = gamma^2 + sum(alpha.^2 + beta.^2)

end

